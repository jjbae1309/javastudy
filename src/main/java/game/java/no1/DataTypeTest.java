package game.java.no1;

public class DataTypeTest {
	public static void main(String[] args) {
		
		int i;
		float f;
		double d;
		char c;
		boolean b;
		
		i = 120;
		f = 12.23f;
		d = 12.23;
		c = 'a';
		b = true;
		
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
	}
}
